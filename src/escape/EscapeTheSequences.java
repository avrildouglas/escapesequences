	    System.out.print("\t");
	    System.out.print("   ***\r\t\n\t");
	    System.out.print("  *   *\r\t\n\t");
	    System.out.print(" *     *\r\t\n\t");
	    System.out.print(" *     *\r\t\n\t");
	    System.out.print("  *   *\r\t\n\t");
